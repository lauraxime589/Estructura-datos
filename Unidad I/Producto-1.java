package Collections.Casos_de_estudio.Ventas_masivas;

public class Producto {
    
    String codigo;
    String nombre;
    double precio;
    String categoria;

    public Producto(String codigo, String nombre, double precio, String categoria) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.precio = precio;
        this.categoria = categoria;
    }
    
}
